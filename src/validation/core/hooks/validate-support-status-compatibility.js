function createHookResult(overrides = {}) {
  return {
    hookName: "validateSupportStatusCompatibility",
    passed: true,
    errorCodes: [],
    warnings: [],
    computedAnswer: null,
    normalizedInput: null,
    notes: "",
    ...overrides
  };
}

export function validateSupportStatusCompatibility(itemStatus, patternStatus) {
  const compatibleStatuses = COMPATIBILITY_MAP[patternStatus];
  const passed = Boolean(
    itemStatus
    && patternStatus
    && compatibleStatuses
    && compatibleStatuses.has(itemStatus)
  );

  return createHookResult({
    passed,
    errorCodes: passed ? [] : [ERROR_CODES.E_SUPPORT_STATUS_MISMATCH],
    normalizedInput: {
      itemStatus: itemStatus ?? null,
      patternStatus: patternStatus ?? null
    }
  });
}
